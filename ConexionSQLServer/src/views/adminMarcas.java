    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String marca  = txtNewMarca.getText();
        if( marca.isEmpty() ){
            JOptionPane.showMessageDialog(null, "Ingresa el nombre de la marca", "Error", JOptionPane.ERROR_MESSAGE);
            txtNewMarca.requestFocus();
        }
        else if( Validations.validarNombres(marca) ){
            JOptionPane.showMessageDialog(null, "Ingresa valores correctos", "Error", JOptionPane.ERROR_MESSAGE);
            txtNewMarca.requestFocus();
        }
        else if( Validations.lenght(marca, 20) ){
            JOptionPane.showMessageDialog(null, "M√°ximo se aceptan 20 caracteres", "Error", JOptionPane.ERROR_MESSAGE);
            txtNewMarca.requestFocus();
        }
        else{
            if( Validations.existMarcaInactive(marca) ){
                JOptionPane.showMessageDialog(null, "Ya existe dicha marca, pero est√° dada de baja, te recomendamos que la habilites!!", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
                txtNewMarca.requestFocus();
            }
            else if( Validations.existMarcaActive(marca) ){
                JOptionPane.showMessageDialog(null, "Ya existe dicha marca, a√±ade una diferente!!", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
                txtNewMarca.requestFocus();
            }
            else{
                try{
                    storedProcedures.addMarca(marca);
                    JOptionPane.showMessageDialog(null, "Marca almacenada exitosamente!!" , "Genial" , JOptionPane.INFORMATION_MESSAGE);
                    txtNewMarca.setText("");txtNewMarca.setEnabled(false);
                    btnGuardar.setEnabled(false);
                    showMarca();
                }
                catch( SQLException e ){
                    JOptionPane.showMessageDialog(this, "Hubo un error al almacenar la marca, intenta de nuevo", ":( FATAL!!", JOptionPane.ERROR_MESSAGE); 
                }
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void tableMarcaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMarcaMouseClicked
        btnGuardar.setEnabled(false);
        btnEliminar.setEnabled(true);
        btnActualizar.setEnabled(true);
        txtNewMarca.setEnabled(true);
        
        try {
            int row = tableMarca.getSelectedRow();
            setCod(Integer.parseInt(tableMarca.getValueAt(row, 0).toString()));
            res = conexionsqlserver.ConnectionDB.Query("select * from marca where cod_marca="+getCod());
            while(res.next()){
                txtNewMarca.setText(res.getString("nombre_marca"));
            }
        } 
        catch (SQLException ex) {}
    }//GEN-LAST:event_tableMarcaMouseClicked

