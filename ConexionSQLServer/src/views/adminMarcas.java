    public void showMarcaBaja(){
        DefaultTableModel marca = (DefaultTableModel) tableBajaMarcas.getModel();
        marca.setRowCount(0);
        res = conexionsqlserver.ConnectionDB.Query(
            "SELECT * FROM marca where marca.activo="+0+" order by nombre_marca"
        );
        
        try{
            while(res.next()){
                Vector v = new Vector();
                v.add(res.getInt("cod_marca"));
                v.add(res.getString("nombre_marca"));
                marca.addRow(v);
                tableBajaMarcas.setModel(marca);
            }
        }
        catch(SQLException e){
        }
    }
    
