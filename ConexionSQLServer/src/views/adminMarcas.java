    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int op = JOptionPane.showConfirmDialog(this, "¬øEst√°s seguro de dar de baja √©sta marca?" , "Responder", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if( op == JOptionPane.NO_OPTION ){
            JOptionPane.showMessageDialog(null, "Proceso cancelado" , "Advertencia", JOptionPane.WARNING_MESSAGE);
            txtNewMarca.requestFocus();
        }
        else{
            try{
                storedProcedures.deleteMarca(getCod());
                JOptionPane.showMessageDialog(null, "Baja exitosa!!", "Genial" , JOptionPane.INFORMATION_MESSAGE);
                txtNewMarca.setText("");txtNewMarca.setEnabled(false);
                showMarca();
                showMarcaBaja();
                btnActualizar.setEnabled(false);
                btnEliminar.setEnabled(false);
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Huno un error, intenta de nuevo!!", "Error" , JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

