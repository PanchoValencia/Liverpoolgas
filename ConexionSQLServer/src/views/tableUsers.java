    private void deleteUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteUserActionPerformed
        
        int row = tableShowUsers.getSelectedRow();
        int opt = JOptionPane.showConfirmDialog(this, "¿Estás seguro de borrar dicho usuario?", "Advertencia", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if(opt == JOptionPane.YES_OPTION){
            try{
                storedProcedures.eliminarUsuario(Integer.parseInt(tableShowUsers.getValueAt(row, 0).toString()));
                this.refresUsers();
            }
            catch(SQLException e){
            }
        }
    }//GEN-LAST:event_deleteUserActionPerformed

