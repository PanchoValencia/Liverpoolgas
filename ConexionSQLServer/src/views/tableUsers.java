    public void refresUsers(){
        DefaultTableModel modelo = (DefaultTableModel) tableShowUsers.getModel();
        modelo.setRowCount(0);
        res = conexionsqlserver.ConnectionDB.Query("select cod_user,username from usuario");
        
        try{
            while(res.next()){
                Vector v = new Vector();
                v.add(res.getInt(1));
                v.add(res.getString(2));
                modelo.addRow(v);
                tableShowUsers.setModel(modelo);
            }
        }
        catch(SQLException e){
        }
    }
    

